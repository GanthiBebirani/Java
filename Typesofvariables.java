public class Typesofvariables {
    public static void main(String args[]){
        int c=10;
        int b=20; 
        String a="Hello World";
        String d="Nani";
        System.out.println(a);
        System.out.println(b+c);//Adding two integers
        System.out.println(a+" "+c);//Concatening integer and String
        System.out.println(a+d);//Two strings are added - concation of two strings

        //Declare multiple Varibles 
         int e=10;
         int f=20;
         int g=30;
         System.out.println(e+" "+f+" "+g);
         int h=10,i=20,j=30;
         System.out.println(h+" "+i+" "+j);
         //single values to Multiple varible
         int l,k,m;
         l=k=m=50;
         System.out.println(l+" "+k+" "+m);

         int Data=78;
         System.out.println(Data);

    }


    
}
