public class Datatypes {
    public static void main(String args[]){
        int a=10;
        float b=10.789f;
        String c="Heloo";
        char d='A';//Single quotes
        boolean e=true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        byte f=126;
          System.out.println(f);
        short g=1500;
          System.out.println(g);
        int h=358090;
          System.out.println(h);
        long i=356879638;
          System.out.println(i);
        float j=13E5f;   
          System.out.println(j);    //  float j=13e5f; -same answer 
          float k=13.575767f;
          System.out.println(String.format("no of precision five %.3f",k)); //to print precision as we want
         
          double l=23.787989d;
          System.out.println(l);

        
          
        




    }
    
}
